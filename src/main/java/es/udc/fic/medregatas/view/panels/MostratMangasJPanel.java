    private DefaultTableModel rellenarMangas() {

        mangasModel.setRowCount(0);

        for (Manga m : mangas) {
            Object[] o = {m.getIdManga(), m.getMillas()};
            mangasModel.addRow(o);
        }

        return mangasModel;
    }

    /**
     * Inserta todas las posiciones registradas para esa manga, ordenadas por
     * tipo y dentro de cada tipo por puntuaci칩n.
     *
     * @return
     */
    private DefaultTableModel rellenarTablaResultados() {

        if (jTable1.getSelectedRow() != -1) {

            barcosModel.setRowCount(0);
            Map<Barco, String> patrones = new HashMap<Barco, String>();
            Manga manga = mangas.get(jTable1.getSelectedRow());

            //Guardamos la informaci칩n de los patrones
            for (Inscripcion i : inscService.getInscripciones(regata)) {
                patrones.put(i.getBarco(), i.getPatron());
            }


            for (Tipo tipo : regataService.getTiposAsociadosByRegata(regata)) {
                // Para cada uno de los tipos participantes en la regata a침adimos
                // sus barcos a la lista.
                for (Posicion p : regataService.getClasificacionManga(manga, tipo)) {
                    Object[] o = {p.getBarco(),
                        patrones.get(p.getBarco()),
                        tipo.getNombre(),
                        p.getPuntos()};
                    barcosModel.addRow(o);
                }
            }
        }
        return barcosModel;
    }

